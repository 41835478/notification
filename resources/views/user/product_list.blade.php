        }
    });
    //批量刷新商品
    $('#refresh_multi_btn').click(function() {
        var pids = get_pids();
        if(pids != '') {
            user.supply_refresh_product(pids);
        }
    });
     //下架
    $('.remove_product_btn').click(function() {
        user.supply_remove_product($(this).attr('data'));
    });
     //删除
