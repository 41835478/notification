        user.supply_refresh_product($(this).attr('data'));
    });
    //取消推荐
    $(".console_commend_btn").click(function() {
        user.console_commend_product($(this).attr('data'));
    });

});

function get_pids() {
    var obj = $('.selectObj');
    var pids = '';
    obj.each(function() {
        if(this.checked == true) {
            pids += this.value + ',';
        }
    });
    return pids.substr(0,pids.length-1);
}
